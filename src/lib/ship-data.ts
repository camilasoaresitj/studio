export function getShips(): Ship[] {
  if (typeof window === 'undefined') {
    return [];
  }
  try {
    const stored = localStorage.getItem(SHIPS_STORAGE_KEY);
    if (!stored) {
      const initialData = getInitialShips();
      localStorage.setItem(SHIPS_STORAGE_KEY, JSON.stringify(initialData));
      return initialData;
    }
    return JSON.parse(stored);
  } catch (error) {
    console.error("Failed to parse ships from localStorage", error);
    return [];
  }
}

export function saveShips(ships: Ship[]): void {
  if (typeof window === 'undefined') {
    return;
  }
  try {
    localStorage.setItem(SHIPS_STORAGE_KEY, JSON.stringify(ships));
    window.dispatchEvent(new Event('shipsUpdated'));
  } catch (error) {
    console.error("Failed to save ships to localStorage", error);
  }
}
